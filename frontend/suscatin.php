            kehidupan berkeluarga. Beberapa topik yang dibahas antara lain tentang nafkah, tempat tinggal, pergaulan yang baik, serta pengertian tentang mahar dan tanggung jawab bersama dalam keluarga. Program ini bertujuan untuk mengurangi potensi masalah
