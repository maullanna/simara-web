        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kantor Urusan Agama (KUA) Kecamatan Karawang Barat telah berdiri sebelum Kemerdekaan Negara Republik Indonesia yaitu pada tahu 1940. Pada saat itu wilayah Kecamatan Karawang Barat  masih luas, meliputi Majalaya
            dan Karawang Timur. Tahun 2002 dilakukan pemekaran melepas wilayah Majalaya untuk berdiri menjadi Kecamatan mandiri. Kemudian pada tahun 2008 secara resmi wilayah Karawang dibagi dua kecamatan, yaitu Kecamatan Karawang Barat dan Kecamatan
