        <h1>50 Siswa-siswi SMKN 1 Karawang Mengikuti Kegiatan Bimbingan Remaja Usia Sekolah</h1>
        <h4>24 Agustus 2024</h4>
        <ion-icon name="location"></ion-icon>
        <h3>Auditorium Utama Kampus SMKN 1 Karawang</h3>
