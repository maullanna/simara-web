    <div class="konten">
        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sebanyak 50 siswa-siswi SMKN 1 Karawang mengikuti kegiatan Bimbingan Remaja Usia Sekolah (BRUS) yang digelar oleh Kantor Urusan Agama Kecamatan Karawang Barat, bertempat di Auditorium Utama Kampus SMKN 1 Karawang,
            Kamis (29/08/2024). Turut hadir dalam kegiatan BRUS, Kepala Kantor Urusan Agama Kecamatan Karawang Barat, H. Hamid Dulmajid, S.HI, Wakil Kepala Sekolah Bidang Kurikulum SMKN 1 Karawang Hj. Diah Gustanti, ST,.MT, Guru PAI SMKN 1 Karawang, Asep
            Saeful Rohman, S.Ag,.M.Pd , Penyuluh Satpel -KB Kecamatan Karawang barat dan Penyuluh Agama Islam PAIF dan P3K sebagai narasumber.
        </p><br>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Wakil Kepala Sekolah Bidang Kurikulum Hj. Diah Gustanti mengucapkan terima kasih kepada Kepala KUA Karawang Barat yang sudah menggelar Bimbingan pra nikah bagi remaja usia sekolah di SMKN 1  Karawang. "Dengan
            adanya kegiatan ini anak-anak kami siswa siswi SMKN 1 karawang dapat menambah ilmu dan wawasan tentang dampak dari pernikahan dini. Saya berharap siswa siswi yang ikut kegiatan ini juga bisa berbagi ilmu kepada temen temn nya yang tidak ikut
            acara ini," ujarnya.</p><br>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Dalam sesi materi, Penyuluh Agama Islam H. Didin Shabudin menyampaikan bahwa pernikahan dini akan melahirkan berbagai masalah, mulai dari anak stunting, terjadinya percekcokan sampai perceraian, angka kematian
            ibu dan bayi , angka putus sekolah pada anak ,angka kemiskinan yang dapat semakin meningkat akibat adanya pernikahan dini.  Sementara Itu, Kepala KUA H. Hamid Dulmajid menjelaskan salah satu pilar utama dalam program ini adalah penanaman pendidikan
            keagamaan yang kuat. Dengan pemahaman agama yang baik, remaja diharapkan memiliki pegangan moral yang kokoh sehingga mampu menahan diri dari perbuatan yang melanggar norma dan nilai agama. </p><br>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; "Pendidikan agama juga membantu mereka memahami pentingnya menunda pernikahan hingga waktu yang tepat, yaitu ketika mereka sudah siap secara mental, fisik, dan finansial," ucap Hamid menjelaskan. Hamid berharap
            dengan adanya program ini, diharapkan prevalensi pernikahan anak dapat ditekan secara signifikan. Remaja yang teredukasi dengan baik akan memiliki visi dan misi yang jelas dalam hidupnya, serta mampu merencanakan masa depan yang lebih baik
            tanpa terjebak dalam pernikahan dini yang seringkali membawa banyak masalah.</p>

